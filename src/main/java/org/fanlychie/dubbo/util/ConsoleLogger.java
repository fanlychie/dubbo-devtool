package org.fanlychie.dubbo.util;

/**
 * 控制台信息
 * Created by Fanlychie on 2018/1/4.
 */
public final class ConsoleLogger {

    public static void log(String message) {
        System.err.println(message);
    }

    public static void logNoProvider() {
        log("==================================================================================================");
        log("##################################===>> 未发现本地提供者服务 <<===##################################");
        log("==================================================================================================");
    }

    public static void logNoLocalConsume() {
        log("==================================================================================================");
        log("#############################===>> 该服务没有直连本地其它提供者服务 <<===#############################");
        log("==================================================================================================");
    }

    public static void logStartupProvider() {
        log("==================================================================================================");
        log("#################################===>> 正在启动本地提供者服务 <<===#################################");
        log("==================================================================================================");
    }

    public static void logStartupConsumer() {
        log("==================================================================================================");
        log("#################################===>> 正在启动本地消费者服务 <<===#################################");
        log("==================================================================================================");
    }

}